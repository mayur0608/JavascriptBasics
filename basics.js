// Print a simple message to the console
console.log("Hello world");

// Declare a variable 'a' and assign an integer value
let a = 8;
console.log(a);                 // Output: 8
console.log(typeof(a));         // Output: 'number' (integer is a type of number in JS)

// Declare a variable 'b' and assign a floating-point number
let b = 234.6;
console.log(typeof(b));         // Output: 'number' (both integers and floats are 'number' type)

// Declare a variable 'c' and assign a string value
let c = "mayur";
console.log(typeof(c));         // Output: 'string'

// Declare a variable 'required' and assign a boolean value
let required = true;
console.log(typeof(required));  // Output: 'boolean'
